//unlike constructors, A class can only have a single destructor.
//If a non-aggregate class type object has no user-declared destructor, the compiler will
// generate a destructor with an empty body. This destructor is called an implicit destructor,
// and it is effectively just a placeholder.


//syntax
#include <iostream>

class Simple
{
private:
    int m_id {};

public:
    Simple(int id)
        : m_id { id }
    {
        std::cout << "Constructing Simple " << m_id << '\n';
    }

    ~Simple() // here's our destructor
    {
        std::cout << "Destructing Simple " << m_id << '\n';
    }

    int getID() const { return m_id; }
};

int main()
{
    // Allocate a Simple
    Simple simple1{ 1 };
    {
        Simple simple2{ 2 };
    } // simple2 dies here

    return 0;
} // simple1 dies here